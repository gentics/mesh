package com.gentics.mesh.graphql;

public class DateTestField implements TestField {

	String name;

	String value;

	public DateTestField(String name, String value) {
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
